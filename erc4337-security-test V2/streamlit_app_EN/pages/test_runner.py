def _render_history() -> None:
    items = filter_run_history(st.session_state, module="test_runner", limit=50)
    st.markdown("### Test Runner History (Current Session)")
    if not items:
        st.info("No Test Runner records in current session yet.")
        return

    for i, item in enumerate(items):
        rc = item.get("returncode")
        status = "✅" if rc == 0 else "❌"
        title = f"{status} {item.get('timestamp', 'N/A')} | {item.get('action', 'test')} | rc={rc}"
        with st.expander(title, expanded=(i == 0)):
            cmd = item.get("command") or []
            _render_result_block(
                title="History Record Details",
                stdout=item.get("stdout") or "",
                stderr=item.get("stderr") or "",
                command=cmd,
                manifest_path=item.get("manifest_path"),
                returncode=rc,
                key_base=f"history_{i}_{item.get('timestamp', 'na')}",
            )


def _summarize_output(stdout: str, returncode: int | None) -> None:
    lines = [line.strip() for line in (stdout or "").splitlines() if line.strip()]
    summary_lines = [line for line in lines if line.startswith("Summary:") or line.startswith("Validation:") or line.startswith("Loaded dataset:")]
    if returncode == 0:
        if summary_lines:
            for line in summary_lines[-3:]:
                st.success(line)
        else:
            st.success("Execution Completed")
    else:
        if summary_lines:
            for line in summary_lines[-3:]:
                st.warning(line)


def render() -> None:
    _apply_test_runner_style()
    st.subheader("Test Runner")
    root = Path(st.session_state.project_root)
    test_service = TestService(root)
    cfg = ConfigStore(root).load()
    manifest = RunManifest(root)
    ensure_run_history(st.session_state)

    st.info(
        "Frontend currently integrates 5 test paths: Batch, Signature, High Severity, AI Dataset Validation, AI Dataset On-Chain Execution."
    )
    _render_test_guide()

    baseline_tab, regression_tab, dataset_tab = st.tabs(["Baseline Batch", "Specialized Regression", "Dataset Testing"])

    with baseline_tab:
        with st.container(border=True):
            st.markdown("### Batch Test")
            st.caption("Fixed baseline samples + a few extended samples, quickly judge if local deployment and basic protection are normal.")
            batch_count = st.number_input(
                "Batch Random Extension Count",
                min_value=0,
                max_value=1000,
                value=int(cfg.get("batch_count", 8)),
            )
            if st.button("Run Batch Test"):
                res = test_service.run_batch(batch_count)
                path = manifest.record({"module": "test_runner", "type": "batch", "command": res.command, "returncode": res.returncode})
                append_run_history(
                    st.session_state,
                    module="test_runner",
                    action="batch",
                    command=res.command,
                    returncode=res.returncode,
                    stdout=res.stdout,
                    stderr=res.stderr,
                    manifest_path=str(path),
                    max_items=50,
                )
                _render_result_block(
                    title="Batch Test Result",
                    stdout=res.stdout,
                    stderr=res.stderr,
                    command=res.command,
                    manifest_path=str(path),
                    returncode=res.returncode,
                    key_base="batch_result",
                )

    with regression_tab:
        with st.container(border=True):
            st.markdown("### Signature Test")
            st.caption("Fixed signature attack vector regression. Suitable for execution after modifying signature validation, owner, nonce logic.")
            if st.button("Run Signature Test"):
                res = test_service.run_signature()
                path = manifest.record({"module": "test_runner", "type": "signature", "command": res.command, "returncode": res.returncode})
                append_run_history(
                    st.session_state,
                    module="test_runner",
                    action="signature",
                    command=res.command,
                    returncode=res.returncode,
                    stdout=res.stdout,
                    stderr=res.stderr,
                    manifest_path=str(path),
                    max_items=50,
                )
                _summarize_output(res.stdout, res.returncode)
                _render_result_block(
                    title="Signature Test Result",
                    stdout=res.stdout,
                    stderr=res.stderr,
                    command=res.command,
                    manifest_path=str(path),
                    returncode=res.returncode,
                    key_base="signature_result",
                )

        with st.container(border=True):
            st.markdown("### High Severity Scenarios")
            st.caption("First deploy attack contracts, then execute fixed high-risk attack scenarios. Suitable for checking specific exploit paths.")
            if st.button("Run High Severity Scenarios"):
                dres, tres = test_service.run_high_severity()
                path = manifest.record(
                    {
                        "module": "test_runner",
                        "type": "high_severity",
                        "commands": [dres.command, tres.command],
                        "returncodes": [dres.returncode, tres.returncode],
                    }
                )
                combined_stdout = "\n\n".join(
                    [
                        "[deploy_attack.py]",
                        dres.stdout or "(no stdout)",
                        "[test_vulnerability.py]",
                        tres.stdout or "(no stdout)",
                    ]
                )
                combined_stderr = "\n\n".join(
                    [
                        "[deploy_attack.py]",
                        dres.stderr or "",
                        "[test_vulnerability.py]",
                        tres.stderr or "",
                    ]
                ).strip()
                append_run_history(
                    st.session_state,
                    module="test_runner",
                    action="high_severity",
                    command=["python", "scripts/deploy_attack.py", "&&", "python", "tests/test_vulnerability.py"],
                    returncode=max(dres.returncode, tres.returncode),
                    stdout=combined_stdout,
                    stderr=combined_stderr,
                    manifest_path=str(path),
                    max_items=50,
                )
                _summarize_output(combined_stdout, max(dres.returncode, tres.returncode))
                result_tabs = st.tabs(["Deployment Output", "Vulnerability Scenario Output"])
                with result_tabs[0]:
                    _render_result_block(
                        title="deploy_attack.py Result",
                        stdout=dres.stdout,
                        stderr=dres.stderr,
                        command=dres.command,
                        manifest_path=str(path),
                        returncode=dres.returncode,
                        key_base="deploy_attack_result",
                    )
                with result_tabs[1]:
                    _render_result_block(
                        title="test_vulnerability.py Result",
                        stdout=tres.stdout,
                        stderr=tres.stderr,
                        command=tres.command,
                        manifest_path=str(path),
                        returncode=tres.returncode,
                        key_base="vulnerability_result",
                    )

    with dataset_tab:
        with st.container(border=True):
            st.markdown("### AI Dataset Test")
            st.caption("Targeted for external attack datasets. It is recommended to run structural validation first, then on-chain sampling execution.")
            dataset_path = st.text_input(
                "Dataset Path",
                value=cfg.get("ai_clean_dataset_output", "ai-attack-generator/attacks_dataset_1200_clean.json"),
                key="ai_dataset_path_test",
            )

            ai_chain_limit = st.number_input(
                "On-Chain Execution Limit",
                min_value=1,
                max_value=500,
                value=50,
                key="ai_dataset_chain_limit",
            )
            include_legitimate = st.checkbox(
                "Include legitimate samples during on-chain execution",
                value=False,
                key="ai_dataset_chain_include_legitimate",
                help="By default, only execute attack samples with should_be_blocked=true, to avoid legitimate samples polluting results due to local deployment environment mismatches.",
            )
            if st.button("Run AI Dataset Validation"):
                res = test_service.run_ai_dataset_test(dataset_path)
                path = manifest.record({"module": "test_runner", "type": "ai_dataset", "command": res.command, "returncode": res.returncode})
                append_run_history(
                    st.session_state,
                    module="test_runner",
                    action="ai_dataset",
                    command=res.command,
                    returncode=res.returncode,
                    stdout=res.stdout,
                    stderr=res.stderr,
                    manifest_path=str(path),
                    max_items=50,
                )
                _summarize_output(res.stdout, res.returncode)
                _render_result_block(
                    title="AI Dataset Validation Result",
                    stdout=res.stdout,
                    stderr=res.stderr,
                    command=res.command,
                    manifest_path=str(path),
                    returncode=res.returncode,
                    key_base="ai_dataset_validate_result",
                )

            if st.button("Run AI Dataset On-Chain Test"):
                res = test_service.run_ai_dataset_chain(
                    dataset_path=dataset_path,
                    limit=int(ai_chain_limit),
                    include_legitimate=include_legitimate,
                )
                path = manifest.record({"module": "test_runner", "type": "ai_dataset_chain", "command": res.command, "returncode": res.returncode})
                append_run_history(
                    st.session_state,
                    module="test_runner",
                    action="ai_dataset_chain",
                    command=res.command,
                    returncode=res.returncode,
                    stdout=res.stdout,
                    stderr=res.stderr,
                    manifest_path=str(path),
                    max_items=50,
                )
                _summarize_output(res.stdout, res.returncode)
                _render_result_block(
                    title="AI Dataset On-Chain Test Result",
                    stdout=res.stdout,
                    stderr=res.stderr,
                    command=res.command,
                    manifest_path=str(path),
                    returncode=res.returncode,
                    key_base="ai_dataset_chain_result",
                )

    st.divider()
    _render_history()