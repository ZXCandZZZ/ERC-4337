def _apply_test_runner_style() -> None:
    st.markdown(
        """
        <style>
        .stButton > button {
            width: 100%;
        }
        .result-card {
            padding: 0.25rem 0 0.25rem 0;
        }
        .stTextArea textarea {
            white-space: pre-wrap !important;
        }
        div[data-testid="stCodeBlock"] pre {
            white-space: pre-wrap !important;
            word-break: break-word !important;
        }
        </style>
        """,
        unsafe_allow_html=True,
    )


def _render_result_block(
    title: str,
    stdout: str,
    stderr: str = "",
    command: list[str] | None = None,
    manifest_path: str | None = None,
    returncode: int | None = None,
    key_base: str | None = None,
) -> None:
    with st.container(border=True):
        st.markdown(f"### {title}")
        if returncode is not None:
            if returncode == 0:
                st.success("Execution Successful")
            else:
                st.error(f"Execution Failed rc={returncode}")

        if command or manifest_path:
            with st.expander("View Technical Details", expanded=False):
                if command:
                    st.caption("Command")
                    st.code(" ".join(command), language="bash")
                if manifest_path:
                    st.caption(f"Manifest: {manifest_path}")

        output_tabs = st.tabs(["stdout", "stderr"])
        stdout_key = f"{key_base or title}_stdout"
        stderr_key = f"{key_base or title}_stderr"
        with output_tabs[0]:
            st.text_area(
                "stdout",
                value=stdout or "(no stdout)",
                height=260,
                disabled=True,
                label_visibility="collapsed",
                key=stdout_key,
            )
        with output_tabs[1]:
            st.text_area(
                "stderr",
                value=stderr or "(no stderr)",
                height=220,
                disabled=True,
                label_visibility="collapsed",
                key=stderr_key,
            )


